async def update_job_status(
    job_id: str,
    status: str,
    *,
    processed_url: Optional[str] = None,
    error_message: Optional[str] = None,
    processing_time_ms: Optional[int] = None,
) -> None:
    """
    Persist a status transition for *job_id*.

    Keyword-only optional fields (processed_url, error_message,
    processing_time_ms) are included in the payload only when provided,
    so callers never accidentally overwrite existing values with None.

    Raises on DB failure so the caller can handle retry / alerting.
    """
    payload: dict = {
        "status": status,
        "updated_at": datetime.now(timezone.utc).isoformat(),
    }

    if processed_url is not None:
        payload["processed_url"] = processed_url
    if error_message is not None:
        payload["error_message"] = error_message
    if processing_time_ms is not None:
        payload["processing_time_ms"] = processing_time_ms

    try:
        get_supabase().table(_TABLE).update(payload).eq("id", job_id).execute()
        logger.info(
            f"Job status → {status}",
            extra={"job_id": job_id, "status": status},
        )
    except Exception as exc:
        logger.error(
            "update_job_status failed",
            extra={"job_id": job_id, "status": status},
            exc_info=exc,
        )
        raise

async def update_product_image_url(product_id: str, processed_url: str) -> None:
    """
    Write the processed image URL back to the ``image_url`` column of the
    product row so downstream callers always find the latest version.

    Raises on DB failure.
    """
    try:
        get_supabase().table(_TABLE).update({"image_url": processed_url}).eq("id", product_id).execute()
        logger.info(
            "Product image_url updated",
            extra={"product_id": product_id, "processed_url": processed_url},
        )
    except Exception as exc:
        logger.error(
            "update_product_image_url failed",
            extra={"product_id": product_id},
            exc_info=exc,
        )
        raise


async def update_product_generated_images(
    product_id: str,
    generated_urls: list[str],
    *,
    update_image_url: bool = True,
) -> None:
    """
    Persist the concurrently generated image variant URLs for a product.

    Writes the ordered list of public image URLs to the
    ``generated_image_urls`` JSONB column on the product row.

    Parameters
    ----------
    product_id : str
        UUID of the product row to update.
    generated_urls : list[str]
        Ordered list of public image URLs (variant 1 → variant 4).
        Empty strings or None values in the list represent variants that
        failed generation; callers should filter these before passing.
    update_image_url : bool
        When True (default), also updates ``image_url`` to the first
        URL in *generated_urls* so existing consumers remain unaffected.
        Pass False to leave ``image_url`` unchanged.

    Raises
    ------
    Exception
        Re-raises any Supabase error so the caller can handle it.
    """
    if not generated_urls:
        logger.warning(
            "update_product_generated_images called with empty list — skipping",
            extra={"product_id": product_id},
        )
        return

    # IMPORTANT: pass the native Python list directly — PostgREST serialises it
    # to a JSON array automatically.  Passing json.dumps() (a Python string)
    # sends a text literal to a JSONB column which PostgREST rejects.
    payload: dict = {
        "generated_image_urls": generated_urls,   # list[str] → JSONB array
    }

    if update_image_url:
        payload["image_url"] = generated_urls[0]

    try:
        resp = get_supabase().table(_TABLE).update(payload).eq("id", product_id).execute()
        logger.info(
            f"Stored {len(generated_urls)} generated image URL(s) for product",
            extra={
                "product_id": product_id,
                "variant_count": len(generated_urls),
                "urls": generated_urls,
                "db_response_count": len(resp.data) if resp.data else 0,
            },
        )
    except Exception as exc:
        logger.error(
            f"update_product_generated_images FAILED — product_id={product_id} urls={generated_urls}",
            extra={"product_id": product_id},
            exc_info=exc,
        )
        raise