async def fetch_pending_job() -> Optional[dict]:
    """
    Atomically claim one pending job by transitioning its status to
    'processing'.  Uses an optimistic-lock pattern:

      1. SELECT the oldest pending job (by created_at).
      2. UPDATE it to 'processing' only while it is still 'pending'
         — this guards against two workers claiming the same row.

    Returns the claimed job dict, or None when the queue is empty or
    another worker won the race.
    """
    try:
        # ── Step 1: find the oldest pending job ──────────────────────────
        select_resp = (
            get_supabase().table(_TABLE)
            .select("id")
            .eq("status", "pending")
            .order("created_at", desc=False)
            .limit(1)
            .execute()
        )

        if not select_resp.data:
            return None

        job_id: str = select_resp.data[0]["id"]

        # ── Step 2: claim it atomically with an optimistic lock ───────────
        now = datetime.now(timezone.utc).isoformat()
        update_resp = (
            get_supabase().table(_TABLE)
            .update({"status": "processing", "processing_started_at": now})
            .eq("id", job_id)
            .eq("status", "pending")   # only succeeds if still pending
            .execute()
        )

        if not update_resp.data:
            # Another worker claimed it first — harmless contention
            return None

        job = update_resp.data[0]
        logger.info("Claimed job", extra={"job_id": job_id})
        return job

    except APIError as exc:
        if exc.code in _FATAL_PGRST_CODES:
            # Re-raise so the worker can apply a long back-off instead of
            # hammering Supabase with requests every 2 seconds.
            raise
        logger.error("fetch_pending_job API error", exc_info=exc)
        return None
    except Exception as exc:
        logger.error("fetch_pending_job failed", exc_info=exc)
        return None


async def fetch_job_by_id(job_id: str) -> Optional[dict]:
    """Return a full job row by primary key, or None if not found."""
    try:
        resp = (
            get_supabase().table(_TABLE)
            .select("*")
            .eq("id", job_id)
            .limit(1)
            .execute()
        )
        return resp.data[0] if resp.data else None

    except Exception as exc:
        logger.error(
            "fetch_job_by_id failed",
            extra={"job_id": job_id},
            exc_info=exc,
        )
        return None


async def reset_stale_jobs(timeout_seconds: int) -> int:
    """
    Detect jobs stuck in 'processing' beyond *timeout_seconds* and
    reset them to 'pending' so the worker will retry them.

    Returns the number of rows reset.
    """
    try:
        cutoff = (
            datetime.now(timezone.utc) - timedelta(seconds=timeout_seconds)
        ).isoformat()

        resp = (
            get_supabase().table(_TABLE)
            .update({"status": "pending", "processing_started_at": None})
            .eq("status", "processing")
            .lt("processing_started_at", cutoff)
            .execute()
        )

        count = len(resp.data) if resp.data else 0
        if count:
            logger.warning(f"Reset {count} stale job(s) back to pending")
        return count

    except APIError as exc:
        if exc.code in _FATAL_PGRST_CODES:
            raise
        logger.error("reset_stale_jobs failed", exc_info=exc)
        return 0
    except Exception as exc:
        logger.error("reset_stale_jobs failed", exc_info=exc)
        return 0


